/**
 * apiMessages.ts — API安全文面マスター
 *
 * 全APIルートのエラーメッセージはここに集約する。
 * フロントに表示される文言の一元管理により:
 *   - 生エラーの漏洩を構造的に防止
 *   - メッセージ変更時に1箇所修正で完了
 *   - 全文面のレビュー・監査が容易
 *
 * ルール:
 *   - スタックトレース、DB名、カラム名、内部IPを含めてはならない
 *   - ユーザーが「何をすべきか」分かる文言にする
 *   - 定型文は関数ではなく文字列定数で定義する
 */

